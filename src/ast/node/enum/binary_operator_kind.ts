/**
 * Enum representing different kinds of binary operators.
 */
export enum BinaryOperatorKind {
  /** Assignment operator */
  ASSIGNMENT,
  /** Multiplication operator */
  MULTIPLY,
  /** Division operator */
  DIVIDE,
  /** Modulus operator */
  MODULUS,
  /** Addition operator */
  ADD,
  /** Subtraction operator */
  SUBTRACT,
  /** Shift left operator */
  SHIFT_LEFT,
  /** Shift right operator */
  SHIFT_RIGHT,
  /** Less than operator */
  LESS_THAN,
  /** Less than or equal operator */
  LESS_THAN_OR_EQUAL,
  /** Greater than operator */
  GREATER_THAN,
  /** Greater than or equal operator */
  GREATER_THAN_OR_EQUAL,
  /** Equal operator */
  EQUAL,
  /** Not equal operator */
  NOT_EQUAL,
  /** Bitwise AND operator */
  BITWISE_AND,
  /** Bitwise OR operator */
  BITWISE_OR,
  /** Logical AND operator */
  LOGICAL_AND,
  /** Logical OR operator */
  LOGICAL_OR,
}
